function hexagon(){
	var start_left=0;
	var outer=$(".hexagon-outer-box");
	var obj=$(".hexagon-item");
	var hex_num=obj.length;
	var item_pd=0;

	if(hex_num>1) item_pd=hexagon_padding;

	var row_w=outer.width();
	var item_w=Number(obj.width())+item_pd;
	var item_h=Number(obj.height());
	var hex_w=item_w*hex_num;
	
	var row_num=Math.floor(row_w/item_w); //1열에 들어갈 수 있는 최대 갯수
	if(hexagon_row>0 && row_num>hexagon_row){
		row_num=hexagon_row;
	}
	if(row_num<1) row_num=1; //최소 1개는 나와야함

	var start_left=((row_w-(item_w*row_num))/2)-(item_pd+(item_pd/2));
	
	var r=0;
	var d=0;
	var h=(item_pd*2);
	var max=row_num;
	var top=0;
	var left=0;

	obj.each(function(i){
		if(row_num>1){
			top=r*(item_w-(item_pd*2));
		}else{
			top=r*(item_w+item_pd);
		}

		left=(d*item_w)+h+start_left;

		$(this).css({"top":top+"px","left":left+"px"});
		
		d ++;
		
		if(d==max){
			r ++;
			d=0;
			if(r%2!=0 && max>1){
				max=row_num-1;
				h=(item_w/2)+(item_pd*2);
			}else{
				max=row_num;
				h=item_pd*2;
			}
		}
	});

	if(r<1) r=1;

	outer.css({"height":(r*item_h)+"px"});
}
