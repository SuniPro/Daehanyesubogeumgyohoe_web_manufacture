                <li class="li">대한예수교복음교회가 믿는 것은 다음과 같다. </li>
                <li>1. 성 경 : 성경은 하나님의 영감으로 된 것을 믿는다. (딤후 3:16-17)</li>
                <li>2. 하나님 : 하나님은 성부, 성자, 성령 삼위일체이신 것을 믿는다. (요일 5:7)</li>
                <li>3. 인간의 타락 : 인간은 하나님의 형상대로 창조되었으나 스스로 불순종하여 완전한데서 타락되었음을 믿는다. (롬 5:12)</li>
                <li>4. 대속의 계획 : 그리스도께서 우리가 아직 죄인 되었을 때에 우리를 대신하여 죽으심으로 그를 믿는 자마다 용서받게 된다는 것을 믿는다. (롬 5:8)</li>
                <li>5. 은혜를 통한 구원 : 의롭지 못한 우리가 하나님께 나아오려면 반드시 그리스도의 의로우심을 힘입어야 함을 믿는다. </li>
                <li>6. 회개와 영접 : 진정으로 회개하고 그리스도께서 나의 죄를 위하여 대속해 주심을 믿고 개인의 구세주로 받아들여 시인할 때 하나님 앞에서 의롭게 되는 것을 믿는다. (요일 1:9)</li>
                <li>7. 중 생 : 우리가 회심하는 순간 내 마음과 생애 속에서 생기게 되는 변화가 대단히 절실한 변화임을 믿는다.</li>
                <li>8. 신자의 일상생활 : 우리가 매일같이 성화되면서 믿음 안에서 점점 자라가야 하는 것이 하나님의 뜻임을 믿는다. (히 6:1)</li>
                <li>9. 세례와 성찬 : 침례로 받는 세례가 내적역사에 대한 외적 증거인 것을 믿는다. 또한, 성찬식에서 떡과 포도즙을 상징적으로 사용하므로 주님의 성찬을 기념하는 것을 믿는다. (마 28:19, 고전 11:28)</li>
                <li>10. 성령세례 : 우리는 성령세례가 신자에게 능력을 부여하는 것임을 믿고 또한 성령을 받는 방법은 사도행전의 것과 같음을 믿는다. (행 2:4)</li>
                <li>11. 성령 충만한 생활 : 매일 성령 안에서 살아야 하는 것이 하나님의 뜻임을 믿는다. (갈 5:16, 25)</li>
                <li>12. 성령 은사와 열매 : 성령께서 은사를 신자에게 주심을 믿고 우리가 성령 충만한 생활의 증거로 성령의 열매   를 보여 주어야 하는 것을 믿는다. (고전 12:1-11, 갈 5:22)</li>
                <li>13. 중 도 : 신자의 체험이나 매일의 생활이 너무 극단적이거나 광신적이어서는 안됨을 믿는다.</li>
                <li>14. 하나님의 치료하심 (신유) : 하나님이 병 고치는 것은 믿음의 기도에 응답하여 치료하시는 그리스도의 능력으   로 말미암는 것을 믿는다. (약 5:14-16)</li>
                <li>15. 그리스도의 재림 : 그리스도의 재림이 개인적이고 임박한 사실임을 믿는다. (살전 4:16,17)</li>
                <li>17. 교회와 정부 : 우리는 통치자들이 하나님의 뜻에 반대되는 일을 하는 경우를 제외하고는 언제든   지 받아들여져야 한다는 것을 믿는다. (롬 13:1-5)</li>
                <li>18. 심 판 : 우리는 앞으로 하나님의 심판의 자리 앞에 서게 될 것이며 거기에서 영원한 생명   이나 영원한 사망중 하나를 받게 될 것을 믿는다. (고후 5:10)</li>
                <li>19. 하나님 나라 : 다시 태어난 신자들의 영원한 집은 하나님 나라인 것을 믿는다. </li>
                <li>20. 지 옥 : 지옥은 그리스도를 구세주로 영접하지 않고 거절한 자가 영원한 고통을 받게 될 장소   인 것을 믿는다. (계 20:10, 15)</li>
                <li>21. 전 도 : 우리는 영혼을 그리스도에게 인도하는 것이 지상에 있는 교회의 가장 큰 오직 하나   의 과업인 것을 믿는다. (약 5:20)</li>
                <li>22. 십일조와 헌물 : 우리는 하나님께서 하나님의 뜻을 뒷받침하고 전파하기 위해서 하나님이 정하여 주   신 방법인 십일조를 성도의 의무이며 축복의 근원이기도 함을 믿고, 십일조와 자원   하는 헌물을 통해서 교회를 섬기게 하는 것임을 믿는다. (말 3:10; 고후 9:7)</li>
            </ul>
