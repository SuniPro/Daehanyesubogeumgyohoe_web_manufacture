            <ul>                
                <li>1. 운영위원 : 담임목회자가 임명하며 그 임기는 1년으로 한다.</li>
                <li>2. 집사 : 담임목회자가 임명하며 그 임기는 1년으로 한다.</li>
            </ul>
            <p class="tbold">제53조 (장로)</p>
            <ul>
                <li>1. 자격 :</li>
                <p class="ml_40">
                    1) 구원의 확신을 가진 자로 침례를 받은 자.<br>
                    2) 성령세례를 받은 자.<br>
                    3) 딤전 3:1-7에 저촉되지 않는 자.<br>
                    4) 담임목회자를 도와 영적인 사역을 할 수 있는 자.<br>
                    5) 한 교회에서 권사 또는 안수집사로서 2년 이상의 충실한 신앙생활을 한 자. (단. 건전한 교단에서 이명한 경우 개 교회 규정에 의하여 전직경력을 인정할 수 있다. 이 법 시행 이전의 집사 경력은 안수집사 경력으로 인정할 수 있다)<br>
                    6) 연령이 만40세 이상인 자.
                </p>
                <li>2. 선임</li>
                <p class="ml_40">
                    전항의 자격을 갖춘 자로서 다음과 같은 방법과 절차에 의하여 장로가 된다.<br>
                    1) 침례받은 교인으로 18세 이상인 자 30명당 장로 1인을 둘 수 있다.<br>
                    2) 담임 목회자의 추천을 받은 자.<br>
                    3) 공동의회에서 출석회원 3분의 2이상의 찬성을 받은 자.<br>
                    4) 본 회의 장로고시에 합격한 자. 단, 교단교리과정 이수로 대체할 수 있다.<br>
                    5) 공중예배시 소속지방회 안수위원회에 의하여 장립된 자.
                </p>
                <li>3. 구분</li>
                <p class="ml_40">
                    1) 시무장로(이하 ‘장로’라 함) : 전 1,2항의 규정에 의하여 장로로 시무하고 있는 자로서 만70세 이하인자 (제40조 연령규정준용)<br>
                    2) 원로장로 : 한 교회에서 장로로 15년 이상 시무하고 은퇴한 자로서 운영위원회의 추천을 받아 담임목회자가 추대한 자. <br>
                    3) 은퇴장로 : 장로로서 퇴직한 자. <br>
                    4) 협동장로 : 타 교회 장로로서 소속 교회에 이적하여 교회 운영위원회의 결의를 거쳐 담임목회자가 인정한 자. (단, 협동장로는 본 교단 교리과정을 이수하고 공동의회 과반수이상의 신임결의로 시무장로로 취임할 수 있다.)<br>
                    5) 명예장로 : 담임 목회자는 교회 발전을 위해 필요한 경우 명예장로를 둘 수 있다.
                </p>                
            </ul>
            <p class="tbold">제54조 (권사)</p>
            <ul>
                <li>1. 자격</li>
                <p class="ml_40">
                    1) 구원의 확신을 가진 자로 침례를 받은 여자<br>
                    2) 성령세례를 받은 자<br>
                    3) 딤전 3:1-7에 저촉되지 않는 자 <br>
                    4) 담임 목회자를 도와 영적인 사역을 도울 수 있는 자<br>
                    5) 집사로서 5년 이상 충실하게 신앙생활을 한 여자 (단, 건전한 교단에서 이명한 경우 개 교회 규정에 의하여 전직경력을 인정할 수 있다.) <br>
                    6) 연령이 만 40세 이상인 자
                </p>
                <li>2. 선임</li>
                <p class="ml_40">
                    1) 담임 목회자의 추천을 받은 자<br>
                    2) 공동의회에서 출석회원 과반수의 찬성을 받은 자<br>
                    3) 공중 예배에서 임직식을 거쳐 권사가 된 자<br>
                    4) 담임목회자는 교회의 발전을 위해 필요한 경우 명예 권사를 둘 수 있다
                </p>                            
            </ul>
            <p class="tbold">제55조 (안수집사)</p>
            <ul>
                <li>1. 자격 :</li>
                <p class="ml_40">
                    1) 구원의 확신을 가진 자로 침례를 받은 남자.<br>
                    2) 성령세례를 받은 자.<br>
                    3) 딤전 3:1-7에 저촉되지 않는 자.<br>
                    4) 담임 목회자의 영적 사역을 도울 수 있는 자.<br>
                    5) 한 교회에서 집사로서 5년 이상 충실한 신앙생활을 한 자. (단, 건전한 교단에서 이명한 경우 개 교회 규정에 의하여 전직경력을 인정할 수 있다.) <br>
                    6) 연령이 만35세 이상인 자.
                </p>
                <li>2. 선임</li>
                <p class="ml_40">
                    1) 담임 목회자의 추천을 받은 자<br>
                    2) 공동의회에서 출석회원 과반수의 찬성을 받은 자.<br>
                    3) 공중예배시 임직식을 거쳐 안수집사가 된 자
                </p>               
            </ul>
            <p class="tbold">제56조 (집사)</p>
            <ul>
                <li>1. 직무</li>
                <p class="ml_40">
                    1) 담임목회자의 임명을 받아 제직회의 회원이 된다.<br>
                    2) 교회를 봉사하고, 헌금을 수납하며, 구제에 관한 일을 한다.
                </p>
                <li>2. 자격</li>
                <p class="ml_40">
                    1) 구원의 확신을 가진 자로 침례를 받은 자.<br>
                    2) 진정한 중생의 경험을 갖고 신앙과 지혜의 분별력이 있는 자.<br>
                    3) 딤전 3:8-10에 해당한 자.<br>
                    4) 신망이 높고 입교 후 2년 이상 충실한 신앙생활을 한 자.<br>
                    5) 공중예배시 임명식을 거쳐 집사가 된다.
                </p>                
            </ul>
            <p class="title_tbold">제2절 공 동 의 회</p>
            <p class="tbold">제57조 (회원)</p>
            <p>공동의회는 복음교회의 최고 의결기관으로서 그 회원은 18세 이상의 침례 받은 자로 개교회에서 등록되고 담임목회자로부터 활동교인으로 인증받은 자로 한다.</p>
            <p class="tbold">제58조 (회의)</p>
            <p>공동의회는 다음과 같이 시행한다.</p>
            <ul>
                <li>1. 정기회의 : 담임목회자는 교회운영의 중요사항을 처리하기 위하여 최소한 년1회 이상 전회원들이 참가하는 정기회의를 소집하여야 하며 늦어도 일주일 전에 공고해야 한다. 담임목회자는 정기회의 의장이 되며 의결 정족수는 참석회원 과반수의 찬성으로 한다.</li>
                <li>2. 임시회의</li>
                <p class="ml_40">
                    가. 담임목회자는 교회를 위해 필요시에 일주일 전의 공고로 임시회의를 소집할 수 있다.<br>
                    나. 회의소집권자 유고시에는 운영위원 과반수가 지방회장의 승인을 받아 임시회의를 소집할 수 있다. 단, 이 경우에는 회의의 목적을 사전에 공고하여야 한다.
                </p>                
            </ul>
            <p class="tbold">제59조 (의결사항)</p>
            <p>공동의회는 다음과 같은 사항을 의결한다.</p>
            <ul>
                <li>1. 예산승인 및 결산보고 단, 결산보고는 제직회에 위임할 수 있다.</li>
