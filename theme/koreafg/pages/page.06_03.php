                <li>3. 교회 재정에 대한 자문</li>
                <li>4. 담임목회자의 청빙 추천 및 사례금 결정</li>
                <li>5. 목회자의 승인을 받아 본 규약에 정해진 정기 혹은 임시총회에 파견할 대의원 결정</li>
                <li>6. 목회자의 승인을 받아 교회의 목적 달성을 위한 보조 사역자의 채용 및 그의 사례금 결정</li>
                <li>7. 교회의 모든 재산 유지와 관리</li>
