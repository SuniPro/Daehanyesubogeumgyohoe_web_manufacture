            <p class="tbold">제9조 (의결사항)</p>
            <ul>
                <li>1. 의장 : 총회장은 모든 정기 및 임시총회의 의장이 된다. 총회장 유고시나 필요한 경우에는 부총회장이 의장이 될 수 있다.</li>
                <li>2. 정족수 : </li>
                <p class="ml_40">
                    1) 개회정족수 : 정기 및 임시총회의 개회정족수는 총회 대의원수의 과반수 등록으로 한다.<br>
                    2) 의결정족수 : 모든 의결은 특별한 규정이 없는 한 출석회원 과반수의 찬성으로 한다.
                </p>
                <li>3. 제척사유 : 본 회의 회원, 임직원은 자신에 관한 의결에 참여하여 표결할 수 없다. 단, 증언, 해명 또는 신상 발언은 필요할 경우 해당 회의 의결을 받아 행할 수 있다.</li>
            </ul>
            <p class="tbold">제10조 (연례보고서)</p>
            <p>다음 기관은 공식적인 업무보고서를 총회에 제출하여야 한다.</p>
            <ul>
                <li>1. 총회 임원회</li>
                <li>2. 총회 각 부서</li>
                <li>3. 지방회</li>
                <li>4. 헌법위원회, 윤리위원회, 안수위원회</li>
                <li>5. 재단법인</li>
                <li>6. 학교법인</li>
                <li>7. 기타 총회가 요구하는 기관</li>
            </ul>
            <p class="tbold">제11조 (총회임원)</p>
            <p>본 회의 임원은 총회장, 국제총회장, 부총회장, 총무, 서기, 회록서기, 회계로 한다.</p>
            <p class="tbold">제12조 (총회장)</p>
            <ul>
                <li>1. 선출과 임기 :</li>
                <p class="ml_40">
                    1) 총회에서 선출하며, 임기는 2년으로 한다. 단 1회에 한하여 연임할 수 있다.<br>
                    2) 임기 중에라도 총회에서 불신임이 의결되면 임기가 중단되며 이는 총회 대의원의 3분의 2이상의 찬성으로 결정된다.
                </p>
                <li>2. 자격 : 복음교회에서 10년 이상 목사로서 목회한 자로 총회의 임원 또는 지방회장의 경력이 있어야 한다.</li>
                <li>3. 권한과 직무 </li>
                <p class="ml_40">
                    1) 총회장은 모든 정기 및 임시총회와 임원회를 소집하며 그 의장이 된다. <br>
                    2) 총회의 행정 및 재정 운용의 최고책임자가 된다.<br>      
                    3) 본 회 산하 모든 위원회나 기관에 자문 할 수 있다.<br> 
                    4) 총회와 개교회 발전을 위하여 필요한 조치를 하며 필요시 지방회 및 교회를 관리 감독한다.
                </p>                
            </ul>
            <p class="tbold">제12조의1 (국제총회장)</p>
            <ul>
                <li>1. 선출과 임기 :</li>
                <p class="ml_40">
                    1) 총회에서 선출하며, 임기는 2년으로 하고 연임할 수 있다.<br>
                    2) 임기 중에라도 총회에서 불신임이 의결되면 임기가 중단되며 이는 총회 대의원의 3분의 2이상의 찬성으로 결정된다.
                </p>
                <li>2. 자격 : 한국교회를 대표하여 미국총회 및 해외교회와 교류가 가능한 지도력이 있는 목사로 한다.</li>
                <li>3. 권한과 직무 :</li>
                <p class="ml_40">
                    1) 미국총회와 E.C.F.C. 북아시아 등 국제관계에 있어서 한국대표의 지위와 자격을 갖는댜. <br>
                    2) 총회장과 업무에 대해 협의하고 예산 지원을 요청할 수 있디.
                </p>
                <li>4. 분쟁조정 : 총회장과 국제총회장 간의 의견 불일치나 분쟁이 발생한 경우 증경총회장단에서 조정한다. </li>                
            </ul>
            <p class="tbold">제13조 (부총회장)</p>
            <ul>
                <li>1. 부총회장은 목사 1인, 장로 1인으로 하며 그 선출과 임기는 제12조 1항의 규정에 준한다.</li>                
                <li>2. 부총회장은 총회장 유고시 그 직무를 대행한다. 부총회장 유고시 그 직무대행자는 총회 임원회의 결의에 따라 임시 집행한다.</li>                
            </ul>
            <p class="tbold">제14조 (총회임원회)</p>
            <ul>
                <li>1. 본 회의 업무를 집행하기 위하여 제11조 임원으로 구성되는 임원회를 둔다. </li>
                <li>2. 권한과 직무 : 본 임원회의 권한과 직무는 아래와 같다.</li>
                <p class="ml_40">
                    1) 각 부서의 업무계획 지도<br>
                    2) 재정관리 및 지원<br>
                    3) 업무수행에 필요한 시행세칙 관리 및 승인<br>
                    4) 총회임원 3분의 2이상의 요청에 의한 임시총회 소집 <br>
                    5) 본 회의 발전을 위한 제반사항 및 위임사항
                </p>
                <li>3. 총무, 서기, 회록서기, 회계의 선출 및 직무</li>
                <p class="ml_40">
                    1) 총무, 서기, 회록서기, 회계는 총회장이 회원 중에서 추천하여 총회의 인준을 받는다. <br>
                    2) 총무는 총회장을 보좌하여 총회 각 부서를 관장하고 지방회를 지원하고 조정하며 총무부장의 직무를 수행한다. <br>
                    3) 서기는 본회의 행정업무 및 문서관리와 제반 회의 개회에 관한 업무를 담당한다. <br>
                    4) 회록서기는 정기 및 임시총회와 임원회의 회의록을 작성 보고하며 제반 회의록을 보존 관리한다. <br>
                    5) 회계는 재정부장의 직무를 수행하며 본회의 예·결산 편성 및 수입 지출 경리 업무를 장리한다. <br>
                    6) 총무, 서기, 회록서기, 회계의 직무에 관한 기타 필요 사항은 내규로 정한다. 
                </p>
                <li>4. 임원의 자격 : 본 헌법 제66조와 제73조 2항을 이행한 교회의 목사 및 장로와 안수집사로 한다.</li>
            </ul>
            <p class="tbold">제15조 (감사)</p>
            <ul>
                <li>1. 본회의 제반업무를 감사하기 위하여 감사 2명을 두되 총회에서 선출하고 그 임기는 2년으로 한다.</li>                
                <li>2. 감사는 회계연도 종료 후 2개월 이내에 감사하여 정기총회에 보고해야 한다. 단, 필요시 감사할 수 있으며 긴급히 보고할 사항이 있을 경우 임시총회의 소집을 요구할 수 있다.</li>                
            </ul>
            <p class="tbold">제16조 (총회부서)</p>
            <ul>
                <li>1. 총회 산하에 총무부, 재정부, 해외선교부, 국내전도부, 교육부, 사회부를 두며 부장은 총회장이 임면한다. 단, 총무부장은 총무, 재정부장은 회계로 한다.</li>                
                <li>2. 전호 부서의 관장 업무 내용과 조직에 관하여 필요사항은 내규로 정한다.</li>                
            </ul>
            <p class="title_tbold">제2절 지  방  회</p>
            <p class="tbold">제17조 (구성)</p>
            <ul>
                <li>1. 목 적 : 총회에서 획정한 지역내의 교회발전과 협력을 위하여 지방회를 두며 교회는 총회에 상신하는 제반 사항에 대하여 지방회를 경유하여야 한다.</li>                
                <li>2. 구 성</li>
                <p class="ml_40">
                    1) 지역내 5개 교회 이상의 단위로 지방회를 구성한다. <br>
                    2) 회원은 지역내의 목회자와 각 교회 대표장로 1인씩으로 하되 장로가 없는 교회에서는 총회 대의원 중 1인으로 한다.<br>
                    3) 지방회의 분리나 조정은 지방회의 요청 또는 총회임원회의 필요시 총회임원회에서 결정하여 정기총회에 보고한다.
                </p>
            </ul>
            <p class="tbold">제18조 (임직원)</p>
            <ul>
                <li>1. 지방회의 임원은 회장 1인, 부회장 1인, 총무, 서기, 회계 각 1인을 두되 회장은 담임목사이어야 한다.</li>
                <li>2. 임직원은 해당 지방회에서 선임하며 임기는 2년으로 하고 연임할 수 있다</li>
            </ul>
            <p class="tbold">제19조 (지방회장)</p>
            <ul>
                <li>1. 지방회장은 해당 지방회에서 선출하여 총회에 보고한다. </li>
                <li>2. 지방회장은 지역내 교회의 발전을 위하여 노력하며, 필요한 경우 교회와 목회자 및 운영위원회를 방문하여 격려, 자문 및 교제와 상호이해를 증진 시킨다.</li>
                <li>3. 지방회장은 관할지역 교회의 비상시 운영위원회를 소집할 수 있다. </li>
                <li>4. 지방회장은 총회에 대하여 그 지방회를 대표하고 직접적 책임을 가지며 요청이있을 경우 필요한 보고서를 제출하여야 한다.</li>
                <li>5. 면직 : 총회장은 지방회장이 임기 중이라도 관할지역 교회의 이익증진에 저해될 경우와 제14조 4항의 규정에 저촉될 경우에 해당 지방회를 소집하여 그 지역내 목회자 재적수의 3분의 2이상의 동의를 얻어 지방회장을 해임하고 잔임기간 중 지방회장 대리를 임명할 수 있다.</li>
                <li>6. 지방회장은 관할 지역 내에서의 전도 및 십자군 집회, 개척교회 설립, 정기적인 목회자 회의를 계획 격려하며 자문에 응하고 조언할 수 있다.</li>
            </ul>
            <p class="tbold">제20조 (지방회 재정)</p>
            <ul>
                <li>지방회의 재정은 회원들의 회비와 특별회비 그리고 총회지원금, 기타 찬조금으로 충당 한다.</li>                
            </ul>
            <p class="tbold">제21조 (회무)</p>
            <p>지방회의 회무는 다음과 같다.</p>
            <ul>
                <li>1. 관내 전도사업과 교회 진흥책</li>
                <li>2. 총회에 상정할 안건의 결정</li>
                <li>3. 지역 교회간 연합사업의 추진</li>
                <li>4. 지방회 제반사항의 총회보고</li>
                <li>5. 기타 지방회 발전에 필요한 사항</li>
            </ul>
            <p class="tbold">제22조 (회의) </p>
            <p>지방회의 회의는 정기회의와 임시회의로 구분하여 다음과 같이 시행한다.</p>
            <ul>
                <li>1. 정기회의 매년 1회, 5월중에 지방회장이 소집한다.</li>
                <li>2. 임시회의 필요시에 지방회장 또는 회원 3분의 2이상의 요청에 의하여 소집할 수 있다.</li>
                <li>3. 회의 의결은 총회 규정에 준한다.</li>                
            </ul>
            <p class="title_tbold">제3절 총회 기구</p>
            <p class="title_tbold">제1관 헌법위원회</p>
            <p class="tbold">제23조 (헌법위원회)</p>
            <ul>
                <li>1. 목적 : 본 회의 헌법과 각종 내규 및 시행세칙의 제정, 개정, 수정 그리고 필요시 유권 해석을 위하여 상설기구로서 헌법위원회를 둔다. (단, 총회부서에 관한 내규의 개·수정은 총회 임원회의 결의로 할 수 있다)</li>
                <li>2. 조직 : 헌법위원은 총회에서 선출하며 총회장을 포함한 5-7명으로 구성하고 그 임기는 4년으로 한다. 본 위원회의 전문성을 제고하기 위하여 위원회내외에서 전문위원 3명을 위촉할 수 있다.</li>                
            </ul>
            <p class="title_tbold">제2관 윤리위원회</p>
            <p class="tbold">제24조 (윤리위원회)</p>
            <ul>
                <li>1. 목적 : 본회 회원 중 본 헌법 제42조 1항 각호의 규정에 저촉된 사항을 심리하기 위하여 윤리위원회를 둔다.</li>
                <li>2. 조직 :</li>
                <p class="ml_40">
                    1) 윤리위원회 위원은 총회에서 1심 및 2심 위원을 선출하며 총회장과 지방회장을 포함하여 7-9인으로 한다. <br>
                    2) 윤리위원의 임기는 2년으로 한다.
                </p>
                <li>3. 권한과 임무 : 윤리위원회의 권한과 임무는 총회의 인준을 받아 내규로 정하며 심의후 처분 내용은 견책.정직․자격취소․해임 및 복직 등으로 한다.</li>
                <li>4. 재심청구</li>
                <p class="ml_40">
                    1) 위 3항의 처분에 불복이 있는 회원은 1심 처분이 있는 날로부터 1개월 이내에 재심을 청구 할 수 있다. <br>
                    2) 총회는 재심청구 심의를 위하여 재심위원회를 두되 1심의 윤리위원을 제외한 5인의 위원으로 한다.
                </p>                
            </ul>
            <p class="title_tbold">제3관 안수위원회</p>
            <p class="tbold">제25조 (안수위원회)</p>
            <ul>
                <li>1. 조직 : 총회장은 안수위원으로 당연직인 자신과 신학대 총장을 포함한 7명 이상의 유효신임장을 소지한 목사 중에서 지명하되 총회의 인준을 받아 구성한다.</li>
                <li>2. 임기 : 안수위원의 임기는 2년으로 한다.</li>
                <li>3. 권한과 임무 :</li>
                <p class="ml_40">
                    1) 안수위원회는 안수규정에 의하여 복음교회 목회자의 자격승인과 안수에 관한 사항을 결의 시행한다.<br>
                    2) 안수 받은 목사에게는 자격승인 또는 안수증서를 발행하되 안수위원회 위원장인 총회장이 서명하여 시행한다. <br>
                    3) 안수규정은 안수위원회에서 작성하여 총회의 인준을 받아야 한다.
                </p>                
            </ul>
            <p class="title_tbold">제4관 은급위원회</p>
            <p class="tbold">제26조 (목회자 은급위원회)</p>
            <ul>
                <li>1. 목적 : 본회 목회자 복지기금의 효율적 운영관리를 위해 목회자 은급위원회를 둔다.</li>
                <li>2. 조직 : 은급위원은 총회에서 선출하며, 위원장은 담임목회자 중에 기금을 효율적으로 운영 관리할 수 있는 자로 하고, 위원은 사회부장을 포함한 7인으로 구성하며, 임기는 4년으로 한다. 본 위원회의 전문성을 제고하기 위하여 위원회 내에 전문위원 3인을 위촉할 수 있다.</li>
                <li>3. 목회자 은급 운영을 위해서 목회자 복지기금 관리규정과 목회자 은급위원회 운영 시행세칙을 둔다.</li>
            </ul>
            <p class="title_tbold">제5관 유지 재단법인</p>
            <p class="tbold">제27조 (법인과 정관)</p>
            <ul>
                <li>1. 본회는 산하에 교단 재산관리를 위하여 재단법인 대한예수교복음교회 유지재단 이사회를 조직 운영한다.</li>
                <li>2. 이사의 자격</li>
                <p class="ml_40">
                    1) 본법 제66조, 73조 2항의 규정을 이행한 목회자<br>
                    2) 현재 유효한 신임장을 소지한 목사와 장로로 하되 본회 창립과 발전에 현저한 공적이 있는 자<br>
                    3) 이사회에서 추천한 자
                </p>
                <li>3. 선임과 임기 :</li>
                <p class="ml_40">
                    1) 이사의 정원은 이사장을 포함하여 5-9인으로 한다. <br>
                    2) 이사장과 이사는 이사회에서 선임하며 총회의 인준을 받는다.
                </p>
                <li>4. 이사회의 권한과 임무</li>
                <p class="ml_40">
                    1) 본 회의 기본재산 및 시설관리  <br>    
                    2) 본 법과 총회에서 위임한 제반 업무의 집행 및 보고<br>
                    3) 기타 본 회의 발전을 위한 제반 사항
                </p>
                <li>5. 법인의 운영에 필요한 정관은 이사회에서 작성하여 총회의 인준을 받아 제정하며, 감독청의 인가를 받아 시행한다.</li>
            </ul>
            <p class="tbold">제28조 (이사회의)</p>
            <p>이사회의는 법인 정관에 따른다.</p>
            <p class="tbold">제29조 (법인감사)</p>
            <ul>
                <li>1. 본 법인 제반 업무를 감사하기 위하여 감사 2명을 두되 제27조 2항의 규정에 의한다.</li>
                <li>2. 감사는 이사회의 소집을 요구할 수 있으며 이사회의에 출석하여 발언할 수 있으나 표결에는 참여할 수 없다. </li>                
            </ul>
            <p class="title_tbold">제6관 학교법인</p>
            <p class="tbold">제30조 (학교법인 정관)</p>
            <ul>
                <li>1. 목적 : 본회는 목회자․선교사․교회 영적지도자의 육성과 재교육 및 본회 교리의 연구․보급과 확장을 위하여 학교법인 복음신학원을 설립 운영한다.</li>
                <li>2. 학교법인 이사회 : 학교법인의 이사 및 감사는 정관에 의하여 이사회에서 선임하며 감독청의 인가를 받아 취임한다.</li>
                <li>3. 정관 : 학교법인의 조직과 운영에 관한 필요 사항은 정관으로 정하되 학교법인 이사회에서 작성하여 총회의 인준을 받아야 하며, 감독청의 인가를 받아 시행한다.</li>                
            </ul>
            <p class="title_tbold">제3장 교 회</p>
            <p class="title_tbold">제1절 복음교회</p>
            <p class="tbold">제31조 (교회구분)</p>
            <ul>
                <li>1. 지역교회 : 지역교회란 주님의 몸 된 교회로 복음을 전파하기 위하여 설립하여 운영되는 교회로서 교회의 설립요건을 충족하는 교회를 말하며 지역교회는 조직교회와 미조직교회로 구분한다. 조직교회는 운영위원회, 제직회, 공동의회가 조직된 교회를 말한다. 교회의 설립요건은 담임목회자와 예배드릴 장소가 있어야 하고, 정기적인 예배를 드릴 수 있어야 한다.</li>
                <li>2. 현장교회 : 현장교회는 직장, 병원, 학교 등에서 복음을 전하기 위하여 설립하는 교회로, 교회의 설립요건을 충족하는 교회를 말한다. (단, 해당 기관장으로부터 예배장소 사용 승낙을 받아야 한다.)</li>
                <li>3. 확장교회 : 확장교회는 교회설립을 목적으로 ‘모체교회’가 설립, 운영하거나 개척을 희망하는 목회자가 개척교회의 규모를 갖추기 전 또는 신학교 재학생이 설립하는 예배처를 말한다.</li>                
            </ul>
            <p class="tbold">제32조 (교회설립)</p>
            <ul>
                <li>1. 교회 개척</li>
                <p class="ml_40">
                    1) 지역 선정 : 복음교회는 그 교회가 위치한 지방회장의 승인에 의하여 창설하되 이미 복음교회가 설립되어 있는 지역에 있어서는 인접교회 목회자의 동의를 받아야 한다. <br>
                    2) 명 칭 : 모든 복음교회는 건물 전면에 대한예수교 복음교회 소속 교회임을 표시하되 동일 지역 내에는 기존 복음교회와 동일한 명칭을 사용할 수 없다.
                </p>
                <li>2. 교회 편입 : 본회에 소속되지 아니한 기존의 교회들이 본 교단에 가입을 원할 때는 본회 사무처리 규칙에 의하여 처리한다.</li>                
            </ul>
            <p class="tbold">제33조 (교회의 권리와 의무)</p>
            <p>모든 복음교회는 다음 사항과 같은 권리와 의무를 가진다.</p>
            <ul>
                <li>1. 권리 : 총회에 대의원을 파송하고 교단과 교회성장을 위한 제안과 각종 지원을 청원할 수 있다.</li>
                <li>2. 의무 :</li>
                <p class="ml_40">
                    1) 교회 내에 “예수 그리스도는 어제나 오늘이나 영원토록 동일하시니라” (히브리서 13:8) 표어 성구를 표시해야 한다. <br>
                    2) 본회의 규약 정책 및 교리에 나타난 모든 계획과 총회의 업무에 협력한다. <br>
                    3) 제73조 1항에 의하여 매월 둘째 주일에 선교헌금을 실시하고, 기타 총회임원회가 정하는 특별헌금을 실시한다. <br>
                    4) 제73조 2항에 의하여 매월 헌금의 10분의 1을 확장헌금으로 총회본부에 송금한다. <br>
                    5) 총회가 요구하는 각종 보고서를 제출한다. <br>
                    6) 교회가 속한 지역 지방회에 가입하여 적극적으로 협조하고 활동한다. <br>
                    7) 복음교회가 총회에 대하여 의무이행을 해태한 경우 임원회의 결의에 의거 필요한 조치를 취할 수 있다.
                </p>                
            </ul>
            <p class="title_tbold">제2절 교회 회원 (교인)</p>
            <p class="tbold">제34조 (교인 구분)</p>
            <ul>
                <li>1. 활동 교인 : 현재 교회에 1년 이상 계속 출석하며 적극 협조하고 있는 자로 한다.</li>
                <li>2. 비활동 교인 : 1항의 규정에 부적당한 자는 비활동 교인이 된다. 비활동 교인은 교회운영에 있어 투표권을 갖지 못한다.</li>                
            </ul>
            <p class="tbold">제35조 (회원자격)</p>
            <p>복음교회 교인의 자격 요건은 다음과 같다.</p>
            <ul>
                <li>1. 진정으로 중생경험의 증거와 경건한 그리스도인</li>
                <li>2. 침례를 받은 자로 13세 이상인 자</li>
                <li>3. 신앙선언에 나타난 복음교회 교리에 동의한 자 </li>
                <li>4. 본법과 규약에 제시된 제 규정을 순종하는 자</li>
                <li>5. 회원 인정 방법: 복음교회 교인은 목회자의 인정을 받아 정기 예배에서 회중 앞에 공개적으로 공인되어야 한다.</li>                
            </ul>
            <p class="tbold">제36조 (권리와 의무)</p>
            <ul>
                <li>1. 활동교인의 권리 :</li>
                <p class="ml_40">
                    1) 교회의 모든 활동에 참여하되 투표권은 만 18세 이상인 자로 한다. <br>
                    2) 어느 직책에 선출될 경우 교회에 봉사한다. <br>
                    3) 성찬식에 참석하며, 공동의회에 참석하여 제안․의결권을 갖는다.
                </p>
                <li>2. 의 무 :</li>
                <p class="ml_40">
                    1) 복음교회 교인은 개교회 담임목회자의 목회운영 방침에 순종하고 적극 협조한다. <br>
                    2) 교인은 교회성장에 필요한 모든 사업과 활동에 적극적으로 참여하고 협력하여야 한다. <br>
                    3) 교인은 목회자나 교회에 유해한 언행을 삼가하여야 한다.<br>
                    4) 복음교회 교인은 예배참석, 기도, 헌금, 전도, 봉사등 교인의 기본적인 의무를 다하여야 한다.
                </p>                
            </ul>
            <p class="tbold">제37조 (이명 및 제적)</p>
            <ul>
                <li>1. 이명</li>
                <p class="ml_40">
                    1) 교인 중 사정에 의하여 타 교회로 이명하고자 하는 자에게는 소속교회 목회자가 서명한 이명증서를 발행할 수 있다.<br>
                    2) 다른 교회에서 전입된 교인들은 이명증서를 제출해야 하며, 이명증서를 제출하지 않을 경우에는 담임목회자의 결정에 의해 교인으로 승인한다.
                </p>
                <li>2. 제적</li>
                <p>복음교회 교인은 다음 사항을 위반했을 때 담임목회자의 결정으로 제적할 수 있다.</p>
                <p class="ml_40">
                    1) 본 회의 신앙선언 또는 헌법과 규약에 고의적으로 거부 또는 위배했을 때<br>
                    2) 고의적으로 비기독교적 혹은 비성서적 행위를 범했을 때<br>
                    3) 교회의 유익에 위배되는 행위를 했을 때
                </p>                
            </ul>
            <p class="title_tbold">제3절 목회자</p>
            <p class="title_tbold">제1관 목회자</p>
            <p class="tbold">제38조 (목회자 구분)</p>
            <ul>
                <li>1. 목사 : 신학교를 졸업하고 본 교단의 안수 규정에 따라 목사 안수를 받은 목회자로서 현재 유효한 신임장을 소지한 자이다.</li>
                <li>2. 원로목사 : 한 교회에서 20년 이상을 시무하고 퇴임한 목사이다. (담임전도사의 시무경력은 시무연한에 포함된다.) 당해 교회에서 공동의회 과반수의 결의로 원로목사에 추대하고 총회는 원로목사의 명예를 준다. 사례금은 개교회에서 결정한다.</li>
                <li>3. 공로목사 : 복음교회에서 20년 이상 목회하고 퇴임한 목사로 현저한 공적이 있는 자이다. 지방회원 과반수의 결의로 공로목사로 추대하고 총회는 공로목사의 명예를 준다.</li>
                <li>4. 은퇴목사 : 은퇴목사는 정년이 되어 퇴임한 목사이다.</li>
                <li>5. 전도사 : 신학교를 졸업하고 복음교회에서 사역하는 목회자로 현재 유효한 신임장을 소유한 자이다.</li>
                <li>6. 기관 목회자 : 본 교단 산하의 신학교나 신학대학원 또는 이와 동등이상의 학교를 졸업하고 본 회가 인정하는 기관에서 사역하는 목회자이다. (교목, 원목, 군목, 경목, 사목, 기도원장등). 단, 퇴임한 목사는 소속교회의 직무와 치리권이 없으며 총회의 임역원을 맡을 수 없으나, 지방회와 총회의 당연직 정회원이 된다.</li>                                
            </ul>
            <p class="tbold">제39조 (목회자 자격)</p>
            <p>본 회의 목회자의 자격 요건은 다음과 같다.</p>
            <ul>
                <li>1. 복음신학교 졸업자 및 이와 동등이상의 학력 소지자</li>
                <li>2. 복음교회에 청빙되어 목회하는 자</li>
                <li>3. 본 교단 헌법과 내규에 동의하는 자</li>
                <li>4. 본 교단의 내규에 의하여 교리과정을 이수한 목회자로서 그 자격을 인정받은 자</li>
                <li>5. 총회의 사업과 활동에 협조하는 자 </li>
                <li>6. 안수규정에 결격사유가 없는 자</li>                
            </ul>
            <p class="tbold">제40조 (목회자의 정년) </p>
            <p>목회자의 정년은 만70세가 되는 해의 연말로 한다. 단, 후임 목회자를 세울 수 없는 미자립교회의 담임목회자는 지방회장의 요청에 의해 총회 임원회 승인으로 예외로 할 수 있다</p>
            <p class="tbold">제41조 (목회자 증명서)</p>
            <ul>
                <li>1. 총회장은 본 헌법과 규약에 정한 요건에 따라 자격 있는 목회자임을 증명하는 유효신임증서를 매년 발급한다.</li>
                <li>2. 목회자는 본 법이 정한 부담금을 납부하여야만 당해년도 유효신임장을 발급받을 수 있으며 목회자 복지기금 운영에 참여할 수 있다.</li>                
            </ul>
            <p class="tbold">제42조 (취소, 탈퇴)</p>
            <ul>
                <li>1. 취소 : 아래와 같은 과오를 범한 목회자는 윤리위원회의 결정으로 그 신임장을 취소한다.</li>
                <p class="ml_40">
                    1) 본 회가 규정한 이단이나 사이비 교파에 가입 또는 그 가르침과 교리를 추종하는 행위<br>
                    2) 비기독교적 행위<br>
                    3) 신앙선언 및 본 헌법과 규약의 제규정에 대한 고의적인 과오 및 거부<br>
                    4) 목회자 직무의 고의적 태만<br>
                    5) 불법, 부도덕한 행위 및 사기행위<br>
                    6) 부도덕적 이혼 또는 이혼 후 배우자 생존시기의 재혼<br>
                    7) 본회 및 복음교회 분열을 조장하는 고의적인 시도<br>
                    8) 본회와 유사한 목적을 가진 여타 기관의 형성 및 연합<br>
                    9) 다른 기관으로부터 안수 및 승인을 받은 경우<br>
                    10) 교회 기물의 파손 및 보관에 대한 고의적인 행위<br>
                    11) 동성 및 비성경적 결혼을 주례하는 행위
                </p>
                <li>2. 탈퇴 : 본회 회원의 자격에서 자발적으로 사퇴 또는 취소당하는 목회자와 사역자는 당연히 탈퇴한 것으로 간주하고, 탈퇴에 따른 공고를 하여야 한다.</li>                
            </ul>
            <p class="title_tbold">제2관 담임목회자</p>
            <p class="tbold">제43조 (담임목회자의 청빙과 위임)</p>
            <p>복음교회의 담임목회자는 당해 교회 운영위원회에서 청빙 추천하여 공동의회 3분의 2이상의 결의를 받은 자로서 지방회장을 경유하여 총회장의 승인을 받은 자로 한다.</p>
            <p class="tbold">제44조 (담임목회자의 임무)</p>
            <p>담임목회자의 임무는 다음과 같다.</p>
            <ul>
                <li>1. 설교, 교육, 예배인도 및 안수집행을 통해 교회 전반에 걸쳐 영적구원, 교회정화 및 신앙생활의 증진에 노력한다.</li>
                <li>2. 성례전을 집행한다. (단, 성례전의 집례는 안수받은 목사로 한다.)</li>
                <li>3. 운영위원회의 의장이 되며 공동의회를 진행한다.</li>
                <li>4. 본 규약에 의하여 개교회에 필요한 임원을 임면한다.</li>
                <li>5. 본회 임원회에서 정한 제 서식에 따라 교회의 제반 현황과 모든 회원에 관한 기록을 작성 보관한다.</li>
                <li>6. 모든 회의에 교회를 대표하여 참석한다. </li>
                <li>7. 목회자의 겸직 제한 당해 교회에서 사례금을 받는 목회자는 운영위원회의 동의를 얻어 겸직할 수 있다. 단, 비성서적 반기독교적인 직종은 종사할 수 없다.</li>
                <li>8. 당해 교회에서 사임하는 목회자는 모든 교인, 사업 및 운영에 관한 제반기록을 후임 목회자에게 반드시 인계하여야 한다.</li>
                <li>9. 담임목회자가 전도사일 경우는 해당 지방회장의 지도를 받는다.</li>                
            </ul>
            <p class="tbold">제45조 (담임목회자의 휴직과 해임)</p>
            <ul>
                <li>1. 휴직 : 목회자는 정상적인 목회활동 없이 1년 이상 휴직할 수 없다. 휴직 기간이 1년을 초과하는 경우에는 소속된 교회가 있어야 목회자로 인정 받을 수 있다. </li>
                <li>2. 해임 : 담임 목회자가 목회를 감당하기에 부적격하여 당해 교회 운영위원회의 소청이 있을 경우 지방회장을 경유하여 윤리위원회의 결의를 거쳐 총회장이 해임한다. </li>                
            </ul>
            <p class="title_tbold">제3관 부목회자</p>
            <p class="tbold">제46조 (임명) </p>
            <p>담임목회자가 목회적 필요에 의하여 추천한 자를 교회 운영위원회에서 임명결의하고 공동의회에 보고하여 부목회자를 채용 할 수 있다.</p>
            <p class="tbold">제47조 (부목회자의 종류)  </p>
            <p>부목회자는 담임목회자를 보좌하며 그 사역의 성격상 다음과 같이 구분한다.</p>
            <ul>
                <li>1) 부목회자 : 목회 전반적인 사역을 보조하는 목회자</li>
                <li>2) 행정목회자 : 교회 행정사역을 담당하는 목회자</li>
                <li>3) 음악목회자 : 찬양과 음악사역을 담당하는 목회자</li>
                <li>4) 교육목회자 : 교회학교 및 평신도 교육을 담당하는 목회자</li>
                <li>5) 심방목회자 : 심방사역을 담당하는 목회자</li>
                <li>6) 협동목회자 : 기관목회자로 분담된 사역을 담당하는 목회자</li>                
            </ul>
            <p class="tbold">제48조 (제한 사항)</p>
            <ul>
                <li>1. 면 직 : 담임목회자의 제안과 교회 운영위원회의 결의에 의하여 면직해임 할 수 있다.</li>
                <li>2. 제한사항 :</li>
                <p class="ml_40">
                    1) 사퇴 : 담임목회자가 정년퇴임하는 경우를 제외하고 사퇴하는 경우 부목회자도 함께 사퇴를 원칙으로 한다. 단 교회 운영위원회의 요청에 따라 새로운 담임목회자가 부임할 때까지 부목회자가 본 법 제44조 1, 2항의 임무는 수행할 수 있다.
                </p>                
            </ul>
            <p class="title_tbold">제4절 선 교 사</p>
            <p class="tbold">제49조 (선교사 구분)</p>
            <ul>
                <li>1. 본부 선교사 : 교단 국제본부로부터 한국 선교활동을 목적으로 파송되어 국내에 상주하는 목회자로 이들은 본 회 해외선교부와 협조하여 사역한다.</li>
                <li>2. 파송 선교사 : 해외 선교를 목적으로 본회에서 국외로 파송한 선교사로 이들은 해외선교부의 지도를 받는다.</li>
            </ul>
            <p class="title_tbold">제4장 교 회 행 정</p>
            <p class="title_tbold">제1절 교회 조직</p>
            <p class="tbold">제50조 (교회기관)</p>
            <p>복음교회의 기구는 다음과 같다.</p>
