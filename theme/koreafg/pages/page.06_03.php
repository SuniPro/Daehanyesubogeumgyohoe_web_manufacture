            <p>태초에 하나님께서는 흑암과 혼돈(混沌)․무(無)와 영(零)의 초자연에서 우주(宇宙)와 실체(實體)․시간과 공간․영계(靈界)와 현상계(現狀界)를 창조하시고 그 가운데 우리들  인간을 하나님의 형상대로 절대적 가치(眞․善․美)를 향유(享有)한 피조물(被造物)로 지으셔서 자연과 역사를 운용(運用)할 수 있는 위대한 존재로 세워 주셨다.</p>
            <p>그러나 인간은 아담의 원죄(原罪) 이후 타락(墮落)하여 하나님의 질서와 체계가 도전(挑戰)받으면서 죄․고통․무질서․마귀와 싸우며 역사의 수레바퀴 속에서 명멸(明滅)할 수밖에 없는 죄인의 삶을 살게 되었다.</p>
            <p>사랑의 하나님은 2,000여년전 인류(人類)를 죄악과 고통에서 구원(救援)하시고자 독생자 예수 그리스도를 보내주시고 십자가의 고난과 보혈에 믿음으로 동참(同參)하는 인간에게 속죄(贖罪)의 길을 열어 주시며 순종과 사랑을 실천궁행(實踐窮行) 하셨다. 예수․십자가․부활의 신앙적 연결은 곧 성령의 열매로 나타났으며 그 성령은 오늘날 인류를 구원할 유일(唯一)의 길이요 목적이 되었다. </p>
            <p>1923년 1월, 미국의 여성 부흥사인 에이미 심플 맥퍼슨(Aimee Semple McPherson)이 로스앤젤레스(Los Angeles)에 앤젤레스 템플(Angelus Temple) 교회를 세우고, 강력한 신유 은사를 나타내며 온 미국 사회에 놀라운 성령 운동을 일으키던 중, 에스겔서 1장 10절에 계시된 4가지 형상을 통해 ‘구원, 성령세례, 신유, 재림’의 4가지 교리를 깨닫고 이 4중 복음을 세계 만방에 선포(宣布)하기 위하여 국제 훠스퀘어 복음교회(International Church of the Foursquare Gospel)을 창설하게 되었다.</p>
            <p>1969년 당시 학교법인 대성학원 이사장 김신옥 목사는 선교의 비전을 가지고 도미하여 유학중 하나님의 계시를 통해 국제 훠스퀘어 복음교회 총재 랄프 맥퍼슨(Rolf K. McPherson)박사와 해외선교총무 렐란드 에드워드(Leland B. Edwards)박사를 만나게 되었고 이들에게 한국의 청소년 선교를 위해 선교사를 파송해 줄 것을 건의하여 그해 9월 필리핀에서 선교활동을 하던 이블린 탐슨 선교사 부부(Arthur & Evelyn Thompson)를 한국에 파송하게 되었다.  이들은 처음 대성학원 산하의 중, 고등학생들을 대상으로 하여 선교활동을 벌였고 한국최초 학생교회(Student Church)를 개설하여 많은 청소년들에게 그리스도의 복음을 심어주었다. 그러던 중 이듬해인 1970년 여름 김신옥 목사가 한국으로 귀국하여 탐슨 선교사 부부와 함께 청소년선교의 활성화와 오순절 성령운동의 확산을 위해서 한국 복음교회 개설을 추진하고 1971년 4월 현 대전광역시 용두동 143-4번지에 부지를 마련하여 대전복음교회를 설립하였다. 그리고 이듬해인 1972년 2월 1일 정부의 허가(문공부 허가 제237호, 종무1-75)를 받아 재단법인 대한예수교 복음교회(The Church of the Foursquare Gospel in Korea)를 창립하기에 이르렀다.</p>
            <p>선교 초기의 한국 복음교회는 복음교회 국제본부에서 파송된 선교사의 감독, 치리를 받는 체계였으나 1984년 5월 29일 국제본부의 인준을 받아 대한예수교 복음교회 총회 헌법을 제정하여 공포 시행함으로서 한국에서의 자주, 자율, 독자성을 확립하고 교회 성장을 위한 일대 도약의 발판을 다지게 되었다.</p>
            <p>대한예수교 복음교회는 창시자가 선언한 구원, 성령세례, 신유, 재림의 4중 복음을 통해 복음전파와 영혼구원, 교회설립과 확장, 영성훈련과 교회지도자 양성, 그리고 세계 선교를 위해 주님의 영광스런 얼굴을 뵈올 때까지 필생의 신념으로 총 매진하기로 하였다.</p>
