            <p class="tbold">제64조 (회의)</p>
            <p>회의는 매월 소집되는 정기회의와 임시회의가 있으며 담임목회자가 1주일 전 공고하여 소집한다. 회의 의결은 출석회원 과반수의 찬성으로 하고 모든 회의내용은 기록하여 보존한다.</p>
